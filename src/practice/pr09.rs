// Функція для перевірки, чи є число простим
fn is_prime(n: &u32) -> bool {
    if *n < 2 {
        return false;
    }
    // Перевіряємо дільники від 2 до квадратного кореня числа
    for i in 2..=(*n as f64).sqrt() as u32 {
        if n % i == 0 {
            return false;
        }
    }
    true
}
#[test]
fn main() {
    // Фіксоване число для перевірки
    let число = 23;

    // Викликаємо функцію для перевірки
    if is_prime(&число) {
        println!("Число {} є простим.", число);
    } else {
        println!("Число {} не є простим.", число);
    }
}
